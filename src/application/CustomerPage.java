    public void kullaniciDataGetir() {
        try {
            String kullaniciDataGetirQuery = "select * from musteriler where mid=" + LoginPage.loginMid;
            ResultSet rs = db.baglan().executeQuery(kullaniciDataGetirQuery);
            lblKullaniciId.setText(rs.getString("mid"));
            lblKullaniciAdi.setText(rs.getString("mAdi"));
            lblKullaniciSoyadi.setText(rs.getString("mSoyadi"));
            lblKullaniciTelefon.setText(rs.getString("mTelefon"));
            lblKullaniciAdres.setText(rs.getString("mAdres"));
            lblKullaniciMail.setText(rs.getString("mMail"));

        } catch (Exception e) {
            System.err.println("kullanici data getirme hatası !s");
        } finally {
            db.kapat();
        }
    }

    public void kullaniciSiparisGetir() {
        try {
            String kullaniciSiparisGetirQuery = "select * from siparisler where smid=" + LoginPage.loginMid ;
            ResultSet rs = db.baglan().executeQuery(kullaniciSiparisGetirQuery);
            DefaultTableModel dtm = new DefaultTableModel();
            dtm.addColumn("Sipariş id");
            dtm.addColumn("Müşteri id");
            dtm.addColumn("Durum");
            dtm.addColumn("Tutar");
            
            while (rs.next()) {
                dtm.addRow(new String[]{rs.getString("sid"), rs.getString("smid"), rs.getString("sDurum"), rs.getString("sTutar")});
            }
            jTableKullaniciSiparisler.setModel(dtm);

        } catch (Exception e) {
            System.err.println("Sipariş Data Getirme Hatası !");
        } finally {
            db.kapat();
        }
    }

